#include "../includes/minirt.h"

int	is_valid_unit(t_vec u)
{
	if (u.x < -1.0 || u.x > 1.0)
		return (0);
	if (u.y < -1.0 || u.y > 1.0)
		return (0);
	if (u.z < -1.0 || u.z > 1.0)
		return (0);
	if (u.x == 0 && u.y == 0 && u.z == 0)
		return (0);
	return (1);
}
